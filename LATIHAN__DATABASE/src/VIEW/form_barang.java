    private void SIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SIMPANActionPerformed
        if(kodebarang.getText().equals("")||
                namabarang.getText().equals("")||
                hbeli.getText().equals("")||
                hjual.getText().equals("")||
                stokbarang.getText().equals(""))
        {  
            JOptionPane.showMessageDialog(null, "Data belum lengkap");
        }
        else
        {
            m_barang data = new m_barang ();
            data.setKodebarang(kodebarang.getText());
            data.setNamabarang(namabarang.getText());
            data.setSatuanbarang(CMBSATUAN.getSelectedItem().toString());
            data.setHargabeli(Integer.parseInt(hbeli.getText()));
            data.setHargajual(Integer.parseInt(hjual.getText()));
            data.setStokbarang(Integer.parseInt(stokbarang.getText()));
            c_barang cbarang = new c_barang();
            cbarang.insertdata(data);
            this.awal();
        }
    }//GEN-LAST:event_SIMPANActionPerformed

