    private void awal()
    {
        kodebarang.setText("");
        namabarang.setText("");
        hbeli.setText("");
        hjual.setText("");
        stokbarang.setText("");
        c_barang cbarang = new c_barang();
        ArrayList<MODEL.m_barang> barang = cbarang.selecdata();
        DefaultTableModel table = (DefaultTableModel)TABEL_BARANG.getModel();
        table.setRowCount(0);
        for (m_barang row : barang)
        {
            Object[] o = new Object[6];
            o[0] = row.getKodebarang();
            o[1] = row.getNamabarang();
            o[2] = row.getSatuanbarang();
            o[3] = row.getHargabeli();
            o[4] = row.getHargajual();
            o[5] = row.getStokbarang();
            table.addRow(o);
        }
    }
    
    private void HAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HAPUSActionPerformed
        int index = TABEL_BARANG.getSelectedRow();
        m_barang mbarang = new m_barang();
        mbarang.setKodebarang(TABEL_BARANG.getValueAt(index, 0).toString());
        c_barang cbarang = new c_barang();
        cbarang.deletedata(mbarang);
        JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
        awal();
    }//GEN-LAST:event_HAPUSActionPerformed

