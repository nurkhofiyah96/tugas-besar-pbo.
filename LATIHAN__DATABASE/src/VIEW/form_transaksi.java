    private void awal()
    {
        String tanggal = new SimpleDateFormat("yyyy-MM-dd").format(new Date());
        tgl.setText(tanggal);
        notransaksi.setText("");
        kodepelanggan.setText("");
        namapelanggan.setText("");
        kodebarang.setText("");
        namabarang.setText("");
        harga.setText("");
        satuanbarang.setText("");
        jumlah.setText("");
        total.setText("");
        
        c_transaksi ctransaksi = new c_transaksi();
        ArrayList<MODEL.transaksitabel> barang = ctransaksi.selecdata();
        DefaultTableModel table = (DefaultTableModel)TABEL_su.getModel();
        table.setRowCount(0);
        for (MODEL.transaksitabel row : barang)
        {
            Object[] o = new Object[9];
            o[0] = row.getNotransaksi();
            o[1] = row.getTgl();
            o[2] = row.getKodepelanggan();
            o[3] = row.getNamapelanggan();
            o[4] = row.getKodebarang();
            o[5] = row.getNamabarang();
            o[6] = row.getHarga();
            o[7] = row.getJumlah();
            o[8] = row.getTotal();
            table.addRow(o);
        }
    }
    
    private void HAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HAPUSActionPerformed
        int index = TABEL_su.getSelectedRow();
        m_transaksi transaksi = new m_transaksi();
        transaksi.setNotransaksi(TABEL_su.getValueAt(index, 0).toString());
        c_transaksi ctransaksi = new c_transaksi();
        ctransaksi.deletedata(transaksi);
        this.awal();
    }//GEN-LAST:event_HAPUSActionPerformed

