    private void SIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SIMPANActionPerformed
        if (notransaksi.getText().equals("") || kodepelanggan.getText().equals("") || kodebarang.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Data belum lengkap");
        }
        else
        {
            m_transaksi transaksi = new m_transaksi();
            transaksi.setNotransaksi(notransaksi.getText());
            transaksi.setTgl(tgl.getText());
            transaksi.setKodepelanggan(kodepelanggan.getText());
            transaksi.setKodebarang(kodebarang.getText());
            transaksi.setHarga(Integer.parseInt(harga.getText()));
            transaksi.setJumlah(Integer.parseInt(jumlah.getText()));
            transaksi.setTotal(Integer.parseInt(total.getText()));
            c_transaksi ctransaksi = new c_transaksi();
            ctransaksi.insertdata(transaksi);
            this.awal();
        }
    }//GEN-LAST:event_SIMPANActionPerformed

