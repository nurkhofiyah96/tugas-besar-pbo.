    private void awal()
    {
        kodepelanggan.setText("");
        namapelanggan.setText("");
        alamat.setText("");
        no_telp.setText("");
        c_pelanggan cpelanggan = new c_pelanggan();
        ArrayList<MODEL.m_pelanggan> pelanggan = cpelanggan.selecdata();
        DefaultTableModel table = (DefaultTableModel)TABEL_pelanggan.getModel();
        table.setRowCount(0);
        for (m_pelanggan row : pelanggan)
        {
            Object[] o = new Object[6];
            o[0] = row.getKodepelanggan();
            o[1] = row.getNamapelanggan();
            o[2] = row.getAlamat();
            o[3] = row.getTeleponpelanggan();
            table.addRow(o);
        }
    }
    
    private void HAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HAPUSActionPerformed
        int index = TABEL_pelanggan.getSelectedRow();
        m_pelanggan mpelanggan = new m_pelanggan();
        mpelanggan.setKodepelanggan(TABEL_pelanggan.getValueAt(index, 0).toString());
        c_pelanggan cpelanggan = new c_pelanggan();
        cpelanggan.deletedata(mpelanggan);
        awal();
    }//GEN-LAST:event_HAPUSActionPerformed

