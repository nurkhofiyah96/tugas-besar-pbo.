    private void SIMPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SIMPANActionPerformed
        if(kodepelanggan.getText().equals("")||
                namapelanggan.getText().equals("")||
                alamat.getText().equals("")||
                no_telp.getText().equals(""))
        {  
            JOptionPane.showMessageDialog(null, "Data belum lengkap");
        }
        else
        {
            m_pelanggan data = new m_pelanggan ();
            data.setKodepelanggan(kodepelanggan.getText());
            data.setNamapelanggan(namapelanggan.getText());
            data.setAlamat(alamat.getText());
            data.setTeleponpelanggan(no_telp.getText());
            c_pelanggan cpelanggan = new c_pelanggan();
            cpelanggan.insertdata(data);
            this.awal();
        }
    }//GEN-LAST:event_SIMPANActionPerformed

